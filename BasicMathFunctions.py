import numpy as np

def add(a,b):
    """Adds a to b
    Parameters
    ----------
    a : int
        first value
    b : int
        second value
    """
    return a + b

def multiply(a,b):
    """Multiplies a to b
    Parameters
    ----------
    a : int
        first value
    b : int
        second value
    """
    return a-b

def sin(x):
    """Sinus of x
    Parameters
    ----------
    x : float
        radiants
    """
    return np.sin(x)